package Ch02;

public class C01진수 {

	public static void main(String[] args) {
		//진수계산법
		//각각 자리의 2진수끼리 합침
		//4		5(8진수	-> 100)
		//100	101	->	100101	-> 37
		//9		A(16진수 -> 1000)
		//1001	1010	->10011010	->	154
		
		//예제
		// 01 진수 개념
		// 02 10->2진수 변환
		// 03 2->10진수 변환
		// 04 문제
		//1		1	1	1	1	1	1	1
		//128	64	32	16	8	4	2	1

//----------------------------------------------------------
//		문제  2진수 -> 10진수
//----------------------------------------------------------
//		10000100(2) 	-> 	128+2		= 130(10)
//		11010100(2) 	-> 	128+64+8+4	= 204(10)
//		10011100(2) 	-> 	128+16+8+4	= 156(10)
//		01011000(2) 	-> 	64+16+8		= 88(10)
//		10011100(2) 	-> 	128+16+8+4	= 156(10)
//				
//				
//		----------------------------------------------------------
//		문제  8진수 -> 10진수
//		----------------------------------------------------------
//		75(8)		->	00111101	->	32+16+8+4+1	= 61(10)
//		45(8)		->	00100101	->	32+4+1		= 37(10)
//		112(8)		->	01001010	->	64+8+2		= 74(10)
//		15(8)		->	00001101	->	16+4+1		= 21(10)
//		67(8)		->	00110111	->	32+16+4+2+1	= 55(10)
//		55(8)		->	00101101	->	32+8+4+1	= 45(10)
//		
//		----------------------------------------------------------
//		문제  16진수 -> 10진수
//		----------------------------------------------------------
//		9A(16)		->	10011010	->128+16+8+2	= 154(10)
//		16(16)		->	00010110	->16+4+2		= 22(10)
//		55(16)		->	01010101	->64+16+4+1		= 85(10)
//		FF(16)		->	11111111	->				= 255(10)
//		D9(16)		->	11011001	->128+64+16+8+1	= 217(10)
//		3B(16)		->	00111011	->32+16+8+2+1	= 59(10)
		
		
		//10진수로 변환(%d)
		System.out.printf("10진수 : %d\n", 0b10101101);	//0b	: 2진수
		System.out.printf("10진수 : %d\n", 173);			//		: 10진수
		System.out.printf("10진수 : %d\n", 0255);			//0		: 8진수
		System.out.printf("10진수 : %d\n", 0xAD);			//0x	: 16진수
		System.out.println();
		
		//8진수로 변환(%o)
		System.out.printf("8진수 : %o\n", 0b10101101);	//0b	: 2진수
		System.out.printf("8진수 : %o\n", 173);			//		: 10진수
		System.out.printf("8진수 : %o\n", 0255);			//0		: 8진수
		System.out.printf("8진수 : %o\n", 0xAD);			//0x	: 16진수
		System.out.println();
		
		//16진수로 변환(%x소문자표기 %X대문자표기)
		System.out.printf("16진수 : %x\n", 0b10101101);	//0b	: 2진수
		System.out.printf("16진수 : %x\n", 173);			//		: 10진수
		System.out.printf("16진수 : %X\n", 0255);			//0		: 8진수
		System.out.printf("16진수 : %X\n", 0xAD);			//0x	: 16진수
		System.out.println();
		
		
	}

}
